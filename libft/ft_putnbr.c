#include "libft.h"

void	ft_putnbr(int i)
{
	if (i >= 0)
		i *= -1;
	else
		ft_putchar('-');
	if (i < -9)
		ft_putnbr(-(i / 10));
	ft_putchar(-(i % 10) + '0');
}
